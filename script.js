function openCase(projectId) {
  const project = projects.find((item) => item.id === projectId);
  if (!project) return;

  opener = document.activeElement;
  renderCase(project);
  dialog.showModal();
  dialog.scrollTop = 0;
  closeDialogButton.focus();
  history.replaceState(null, '', `${pageBaseUrl}#case-${project.id}`);
}

function closeCase() {
  if (!dialog.open) return;
  dialog.close();
  if (location.hash.startsWith('#case-')) history.replaceState(null, '', pageBaseUrl);
  if (opener instanceof HTMLElement) opener.focus();
}

document.addEventListener('click', (event) => {
  const trigger = event.target.closest('[data-open]');
  if (!trigger) return;
  event.preventDefault();
  openCase(trigger.dataset.open);
});

closeDialogButton.addEventListener('click', closeCase);
dialog.addEventListener('click', (event) => {
  const rect = dialog.getBoundingClientRect();
  const clickedOutside = event.clientX < rect.left || event.clientX > rect.right || event.clientY < rect.top || event.clientY > rect.bottom;
  if (clickedOutside) closeCase();
});
dialog.addEventListener('cancel', (event) => {
  event.preventDefault();
  closeCase();
});

const header = document.querySelector('[data-header]');
window.addEventListener('scroll', () => {
  header.classList.toggle('is-scrolled', window.scrollY > 12);
}, { passive: true });

const menuButton = document.querySelector('[data-menu-button]');
const nav = document.querySelector('[data-nav]');
menuButton.addEventListener('click', () => {
  const isOpen = nav.classList.toggle('is-open');
  menuButton.setAttribute('aria-expanded', String(isOpen));
});
nav.addEventListener('click', () => {
  nav.classList.remove('is-open');
  menuButton.setAttribute('aria-expanded', 'false');
});

const reduceMotion = window.matchMedia('(prefers-reduced-motion: reduce)').matches;
if (reduceMotion || !('IntersectionObserver' in window)) {
  document.querySelectorAll('.reveal').forEach((element) => element.classList.add('is-visible'));
} else {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add('is-visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.12 });
  document.querySelectorAll('.reveal').forEach((element) => observer.observe(element));
}

const initialCase = location.hash.match(/^#case-(.+)$/)?.[1];
if (initialCase && projects.some((project) => project.id === initialCase)) {
  window.addEventListener('load', () => openCase(initialCase), { once: true });
}
